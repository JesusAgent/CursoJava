// Elige la combinación ganadora que representa la salida luego de ejecutar el código mostrado: //

public class ejemplo1 {  
    public static void main(String[] args) {  
  
        int[][] matriz = { { 18, 6, 29 },  
                           { 64, 11, 7 },  
                           { 30, 81, 37 },};  
        int c;  
        for (int f = 0; f < matriz.length; f++) {  
            c = f;  
            System.out.printf("%2d ", matriz[f][c]);  
        }  
    }  
}




